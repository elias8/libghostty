/// Physical key codes for layout-independent keyboard input.
///
/// Based on the W3C UI Events KeyboardEvent code standard. Each value
/// represents a physical key position, independent of the keyboard layout.
/// Layout-dependent characters are provided separately as UTF-8 text by the
/// platform.
///
/// ```dart
/// final key = Key.fromNative(event.keyCode);
/// if (key == Key.enter) {
///   print('Enter pressed');
/// }
/// ```
